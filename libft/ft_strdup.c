#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*copy;
	size_t	l;

	i = 0;
	l = ft_strlen(str);
	copy = (char *)malloc(sizeof(char) * (l + 1));
	if (!copy)
		return (NULL);
	while (str[i])
	{
		copy[i] = str[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
